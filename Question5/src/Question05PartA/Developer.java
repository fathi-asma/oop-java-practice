package Question05PartA;

public abstract class Developer implements DeveloperWorks {

    public void work() {
        System.out.println("Developer is working");
    }


    public void eat() {
        System.out.println("Developer is eating");
    }


    public void sleep() {
        System.out.println("Developer is sleeping");
    }


    public void attendMeeting() {
        System.out.println("Developer is attending meeting");
    }


    public void writeCode() {
        System.out.println("Developer is writing code");
    }


}
